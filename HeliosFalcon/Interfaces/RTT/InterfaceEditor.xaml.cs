using System.Windows.Controls;
using System.Text.RegularExpressions;

namespace GadrocsWorkshop.Helios.Interfaces.Falcon.Interfaces.RTT
{
    /// <summary>
    /// UI for RTT Config Generator
    /// </summary>
    public partial class InterfaceEditor : Grid
    {
        public InterfaceEditor()
        {
            InitializeComponent();
        }

        private void TextBoxFPS_PreviewTextInput(object sender, System.Windows.Input.TextCompositionEventArgs e)
        {
            Regex regex = new Regex("[^0-9]");
            e.Handled = regex.IsMatch(e.Text);
        }

        private void TextBoxIPAddress_PreviewTextInput(object sender, System.Windows.Input.TextCompositionEventArgs e)
        {
            Regex regex = new Regex("[^0-9.]");
            e.Handled = regex.IsMatch(e.Text);
        }

        private void TextBoxPort_PreviewTextInput(object sender, System.Windows.Input.TextCompositionEventArgs e)
        {
            Regex regex = new Regex("[^0-9]");
            e.Handled = regex.IsMatch(e.Text);
        }

        private void TextBox_TextChanged(object sender, TextChangedEventArgs e)
        {
            var binding = ((TextBox)sender).GetBindingExpression(TextBox.TextProperty);
            binding.UpdateSource();
        }
    }
}
